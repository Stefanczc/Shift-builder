class Shift {
    
    constructor(shiftName, date, startTime, endTime, hourlyWage, workplace) {
        this.shiftName = shiftName;
        this.date = date;
        this.startTime = startTime;
        this.endTime = endTime;
        this.hourlyWage = hourlyWage;
        this.workplace = workplace;
    }

}

export { Shift };